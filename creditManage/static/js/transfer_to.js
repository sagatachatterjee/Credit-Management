			success:function(response)
			{
				if(response=='success')
				{
					window.location.href='/users/'
				}
				if(response=='error')
				{
					window.alert("Hello")
					window.location.href='/'
				}
			},
			error:function(xhr,textStatus,thrownError)
			{
				window.location.href='/'
			}
